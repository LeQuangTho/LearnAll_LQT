% 3.2.1 -----------------------------------
A1 = double(imread('saturn.png'))/255;
imshow(A1/255);
% 3.2.2 -----------------------------------
N1 = AddPeriodicNoise(A1);
figure
imshow(N1);
PSNR1 = psnr(N1, A1);
% 3.2.3 -----------------------------------
FN1 = fftshift(fft2(N1));
figure
imshow(mat2gray(log(abs(FN1))));
% 3.2.4 -----------------------------------
Z = ones(480, 640)*1;
figure
imshow(Z/255);
% 3.2.5 -----------------------------------
Z(150:200, 290:340) = 0;
Z(280:330, 290:340) = 0;
figure
imshow(Z/255);
% 3.2.6 -----------------------------------
FN2 = FN1 .* Z;
% 3.2.7 -----------------------------------
N2 = ifft2(ifftshift(FN2));
figure
imshow(N2);
PSNR2 = psnr(N2, A1);