package com.lqt.lequangtho;

public class Node {
    private int So;
    private int Hinh;

    public Node(int so, int hinh) {
        So = so;
        Hinh = hinh;
    }

    public int getSo() {
        return So;
    }

    public void setSo(int so) {
        So = so;
    }

    public int getHinh() {
        return Hinh;
    }

    public void setHinh(int hinh) {
        Hinh = hinh;
    }
}
